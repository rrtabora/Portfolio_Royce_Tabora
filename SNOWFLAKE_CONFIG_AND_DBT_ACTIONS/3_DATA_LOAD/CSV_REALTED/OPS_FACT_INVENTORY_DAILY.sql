COPY INTO PORTFOLIO_DB_RAW.RAW.OPS_FACT_INVENTORY_DAILY
FROM @PORTFOLIO_DB_RAW.RAW.CSV_LOAD_STAGE
FILES = ('FACT_INVENTORY_DAILY.csv')
