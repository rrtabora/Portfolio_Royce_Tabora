    FROM TABLE(
        INFER_SCHEMA(
            LOCATION => '@PORTFOLIO_DB_RAW.RAW.CSV_LOAD_STAGE/FACT_INVENTORY_DAILY.csv',
            FILE_FORMAT => 'PORTFOLIO_DB_RAW.RAW.CSV_FORMAT'
        )
    )
